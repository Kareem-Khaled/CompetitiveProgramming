int n; vector<bool>vis;
vector<vector<int>>adj;
ll floyd(int k){
	ll ret = 0;
	for (int u = 0; u < n; u++){
		for (int v = 0; v < n; v++){
			adj[u][v] = min(adj[u][v], adj[u][k] + adj[k][v]);
			if (vis[u] && vis[v])
				ret += adj[u][v];
		}
	}
	return ret;
}
