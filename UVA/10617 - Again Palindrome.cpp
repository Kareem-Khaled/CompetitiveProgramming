ll solve(int l, int r){
	if (l == r)return 1;
	if (l + 1 == r)return 2 + (s[l] == s[r]);
	ll &ret = dp[l][r];
	if (~ret)return ret; ret = 0;
	if (s[l] == s[r])
		ret += solve(l + 1, r - 1) + 1;
	ret += solve(l + 1, r);
	ret += solve(l, r - 1);
	ret -= solve(l + 1, r - 1);
	return ret;
}
