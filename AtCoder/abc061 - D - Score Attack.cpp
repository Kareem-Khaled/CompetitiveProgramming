void billman(){
	vector<ll>dis(n, OO); dis[0] = 0;
	for (int i = 0; i < n - 1; i++){
		bool flag = 0;
		for (auto it : adj){
			if (dis[it.second.second] > dis[it.second.first] + it.first){
				dis[it.second.second] = dis[it.second.first] + it.first;
				flag = 1;
			}
		}
		if (!flag)break;
	}
	vector<bool>negCycle(n);
	for (auto it : adj){
		if (dis[it.second.second] > dis[it.second.first] + it.first){
			dis[it.second.second] = dis[it.second.first] + it.first;
			negCycle[it.second.second] = 1;
		}
		if (negCycle[it.second.first])
			negCycle[it.second.second] = 1;
	}
	if (negCycle[n - 1])cout << "inf";
	else cout << -dis[n - 1];
}
