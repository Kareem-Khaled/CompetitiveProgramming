const ll N = 1e3 + 2, mod = 998244353LL;
ll dp[N][1 << 10][11]; int n; string s;
ll solve(int idx, int mask, int lst){
	if (idx == n)return (mask != 0);
	ll &ret = dp[idx][mask][lst];
	if (~ret)return ret;
	ret = solve(idx + 1, mask, lst) % mod;
	int cur = s[idx] - 'A';
	if (cur == lst || !(mask & (1 << cur)))
		ret += solve(idx + 1, (mask | (1 << cur)), cur) % mod;
	return ret % mod;
}
