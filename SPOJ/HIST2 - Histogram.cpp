int n; vector<int>arr;
pair<int, ll> dp[(1 << 17)][17];
pair<int, ll> solve(int mask, int lst){
	if (mask == (1 << n) - 1)return{ arr[lst], 1 };
	auto &ret = dp[mask][lst];
	if (~ret.first)return ret; ret = { -oo, 0 };
	for (int i = 0; i < n; i++){
		if (((mask >> i) & 1))continue;
		auto cur = solve((mask | (1 << i)), i);
		cur.first += abs(arr[lst] - arr[i]);
		if (ret.first < cur.first)
			ret = cur;
		else if (ret.first == cur.first)
			ret.second += cur.second;
	}
	return ret;
}
