// Problem Link : https://leetcode.com/problems/my-calendar-ii
class MyCalendarTwo {
private:
    bool isOverlap(int start1, int end1, int start2, int end2) {
        return max(start1, start2) < min(end1, end2);
    }

    pair<int, int> getOverlapped(int start1, int end1, int start2, int end2) {
        return {max(start1, start2), min(end1, end2)};
    }
public:
    vector<pair<int, int>> arr, overlap;

    MyCalendarTwo() {}

    bool book(int start, int end) {
        for (auto it : overlap) {
            if (isOverlap(it.first, it.second, start, end)) {
                return 0;
            }
        }

        for (auto it : arr) {
            if (isOverlap(it.first, it.second, start, end)) {
                overlap.push_back(getOverlapped(it.first, it.second, start, end));
            }
        }

        arr.push_back({start, end});
        return true;
    }
};
