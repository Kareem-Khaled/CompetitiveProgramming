/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
    void reorderList(ListNode* head) {
        ListNode* a = head;
        ListNode* half = head;

        // get the half
        while(a && a->next){
            a = a->next->next;
            half = half->next;
        }

        // reverse the second half
        ListNode* pre = NULL;
        while(half){
            ListNode* nxt = half->next;
            half->next = pre;
            pre = half;
            half = nxt;
        }
        half = pre;

        // merge the two halfs
        while(head && half){
            ListNode* nxt = head->next;
            ListNode* nxt2 = half->next;
            head->next = half;
            half->next = nxt;
            head = nxt;
            half = nxt2;
        }

        if (head) head->next = NULL;
    }
};
